package corejavaConcepts;

public class DataTypes {

	public static void main(String[] args) {
		//To store the numbers in a variable
		byte a = 127;
		short b = 128;
		int c = 89232;
		long d = 27302;
		
		//To store the numbers with decimal values in a variable
		double e = 89232.676;
		float f = 89232.676f;
		
		//To store the number/alphabet/special character  in a variable
		char g ='a';
		char h='%';
		char i ='7';
		
		//To store TRUE/FALSE in a variable
		boolean j = true;
		boolean k = false;
		
	}

}
